    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
     jTable1.setValueAt(jTextField1.getText(), baris,0);
     jTable1.setValueAt(jTextField2.getText(), baris,1);
     jTable1.setValueAt(jComboBox2.getSelectedItem(), baris,2);
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        
        String nim_edit = jTable1.getValueAt(baris,0).toString();
        String nama_edit = jTable1.getValueAt(baris,1).toString();
        String angkatan_edit = jTable1.getValueAt(baris,2).toString();
        
        jTextField1.setText(nim_edit);
        jTextField2.setText(nama_edit);
        jComboBox2.setSelectedItem(angkatan_edit);
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();
        model.removeRow(baris);
    }//GEN-LAST:event_jButton2ActionPerformed

